int main() {
    ios::sync_with_stdio(false); cin.tie(0); cout.tie(0);
    int nTest; cin >> nTest;
    while (nTest--) {
        ll n; cin >> n;
        if (n % 2050 != 0) {
            cout << "-1\n";
            continue;
        }
        ll times = n / 2050;
        int ans = 0;
        while (times) {
            ans += times % 10;
            times /= 10;
        }
        cout << ans << '\n';
    }
    return 0;
}